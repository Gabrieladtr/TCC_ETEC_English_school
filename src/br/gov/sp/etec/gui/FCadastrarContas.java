    private void jComboBoxContasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jComboBoxContasFocusLost
        String var1 = jComboBoxContas.getSelectedItem().toString();
        if(var1.equals("Clientes")){
            jComboBoxTipos.removeAllItems();
        for(Object array1 : AtivoClientes()){
            jComboBoxTipos.addItem(array1);}
        }
        else if(var1.equals("Estoques")){
            jComboBoxTipos.removeAllItems();
        for(Object array2: AtivoEstoques()){
            jComboBoxTipos.addItem(array2);}
        }
        else if(var1.equals("Adiantamento")){
            jComboBoxTipos.removeAllItems();
        for(Object array2: AtivoAdiantamentos()){
            jComboBoxTipos.addItem(array2);}
        }
        else if(var1.equals("Imobilizados")){
            jComboBoxTipos.removeAllItems();
        for(Object array2: AtivoImobilizados()){
            jComboBoxTipos.addItem(array2);}
        }
        else if(var1.equals("Contas a pagar")){
            jComboBoxTipos.removeAllItems();
        for(Object array2: PassivoContasPagar()){
            jComboBoxTipos.addItem(array2);}
        }
        else if(var1.equals("Contas a pagar")){
            jComboBoxTipos.removeAllItems();
        for(Object array2: PassivoContasPagar()){
            jComboBoxTipos.addItem(array2);}
        }
        else if(var1.equals("Patrimônio Líquido")){
            jComboBoxTipos.removeAllItems();
        for(Object array2: PassivoPatrimonioLiquido()){
            jComboBoxTipos.addItem(array2);}
        } 
        else{
            JOptionPane.showMessageDialog(null, "Não possui subcategoria");
            jComboBoxTipos.removeAllItems();
        }
    }//GEN-LAST:event_jComboBoxContasFocusLost

