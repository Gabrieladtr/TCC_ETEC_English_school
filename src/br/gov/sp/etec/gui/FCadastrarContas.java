    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        CadastroAtivoDAO dao = new CadastroAtivoDAO();
        if(var.equals("Ativo")){
//        Categoria c = (Categoria) jComboBoxContas.getSelectedItem();
        //
        CadastroContas cadastro = new CadastroContas();
        //
        String descricao = jTextFieldDesc.getText();
        cadastro.setDescricaoA(descricao);
        
        String valor = jTextFieldValor.getText();
        Scanner testeValor = new Scanner(valor);
        if(testeValor.hasNextDouble()){
            cadastro.setValor(testeValor.nextDouble());
        }
        
        Date data = jDateChooser1.getDate();
        DateFormat formatter = new SimpleDateFormat("yyyy/MM/dd");
        String dataFormatada = formatter.format(data);
        cadastro.setData_lancamento(dataFormatada);
        
       
        if(jComboBoxTipos.getItemCount() == 0){
                cadastro.setCategoria(jComboBoxContas.getSelectedItem().toString());
                
        } else{
                cadastro.setCategoria(jComboBoxContas.getSelectedItem().toString());
                cadastro.setSubcategoria(jComboBoxTipos.getSelectedItem().toString());
        }
        dao.insert(cadastro);
        
        
        } else{
            CadastroPassivoDAO pdao = new CadastroPassivoDAO();
//            Categoria c = (Categoria) jComboBoxContas.getSelectedItem();
        //
        CadastroContas cadastro = new CadastroContas();
        
        //
        String descricao = jTextFieldDesc.getText();
        cadastro.setDescricaoA(descricao);
        
        String valor = jTextFieldValor.getText();
        Scanner testeValor = new Scanner(valor);
        if(testeValor.hasNextDouble()){
            cadastro.setValor(testeValor.nextDouble());
        }
        
        Date data = jDateChooser1.getDate();
        DateFormat formatter = new SimpleDateFormat("yyyy/MM/dd");
        String dataFormatada = formatter.format(data);
        cadastro.setData_lancamento(dataFormatada);
        
        if(jComboBoxTipos.getItemCount() == 0){
                cadastro.setCategoria(jComboBoxContas.getSelectedItem().toString());

        } else{
                cadastro.setCategoria(jComboBoxContas.getSelectedItem().toString());
                cadastro.setSubcategoria(jComboBoxTipos.getSelectedItem().toString());
        }
        pdao.insert(cadastro);
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBoxCategoriaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jComboBoxCategoriaFocusLost
        CategoriaDAO cdao = new CategoriaDAO();
        
        var = jComboBoxCategoria.getSelectedItem().toString();
        if(var.equals("Ativo")){
            jComboBoxContas.removeAllItems();
        for(Categoria cat: cdao.selectAtivo()){
            jComboBoxContas.addItem(cat);}
        }
        else{
            jComboBoxContas.removeAllItems();
        for(Categoria cat: cdao.selectPassivo()){
            jComboBoxContas.addItem(cat);}
        }
    }//GEN-LAST:event_jComboBoxCategoriaFocusLost

